public class User {
	
	private int x;
	private int y;
	
	public User(int posX, int posY){
		x=posX;
		y=posY;
	}
	
	public void setX(int newX){
		x=newX;
	}
	
	public void setY(int newY){
		y=newY;
	}
	
	public int getX(){
		return x;
	}
		
	public int getY(){
		return y;
	}
	
	
}
